function makeAllowed($make)
{
    $allowedMakes = allowedMakes();
    if(in_array($make, $allowedMakes)) return true;
    return false;
}

function car_Make_input_list()
{
    $output = '<select name="carMake" class="form-control">';
    $carMakes = \App\Services\CarDataService::allMakesStatic();
    foreach ($carMakes as $carMake)
    {
        $output .= '<option value="'.$carMake->slug.'">'.$carMake->name.'</option>';
    }
    $output .= '</select>';
    return $output;
}

function car_Model_input_list()
{
    $output = '<select name="carModel" class="form-control">';
    $carModels = \App\Services\CarDataService::allModelsStatic();
    foreach ($carModels as $carModel)
    {
        $output .= '<option value="'.$carModel->id.'">'.$carModel->make->name.' : '.$carModel->name.'</option>';
    }
    $output .= '</select>';
    return $output;
}

function selectedTypeCheckbox($type, $request)
{
    if(isset($request->request->listingType)) {
        $ltype = explode(',', $request->request->listingType);
        foreach ($ltype as $value)
        {
            if($value == $type) return 'checked';
        }

    }
}

function searchSelected($type, $request)
{
    if(isset($request->request->$type)) {
        return $request->request->$type;
    }
    return 0;
}

function searchFilterCheckbox($area, $search=null, $filters)
{
    //dd($area, $search, $filters);
    if(isset($filters[$area]))
    {
        if($search == null) return 'checked';
        $ltype = explode(',', $filters[$area]);

        foreach ($ltype as $value)
        {
            if(trim($value) == $search) return 'checked';
        }
    }
}